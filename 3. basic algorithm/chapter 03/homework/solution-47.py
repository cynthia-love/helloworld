"""
    n条直线位于同一平面, 任意两条不平行, 任意三条不相交于一点
    证明n条直线能确定Θ(n^2)个焦点
"""

"""
    n = 1时, 0
    n = 2时, 1
    n = 3时, 1+2
    n = 4时, 1+2+3
    即f(n) = 0+1+2+..+(n-1) = n*(n-1)/2 = 1/2*n^2 -1/2*n
    
    显然: 1/2*n^2 <= 1/2*n^2 -1/2*n <=n^2
    
    所以f(n)为Θ(n^2)
"""